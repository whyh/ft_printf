#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;
	int		i;

	i = -1;
	s = (char *)src;
	d = (char *)dst;
	if (s < d)
		while ((int)(--len) >= 0)
			*(d + len) = *(s + len);
	else
		while (++i < (int)len)
			*(d + i) = *(s + i);
	return (dst);
}
